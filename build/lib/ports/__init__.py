"""Ports — Interfaces structurelles (typing.Protocol) pour les services JARVIS.

Suit le pattern Protocol structurel pour la couche adapter.
Les classes de service existantes (LogService, MetricsService, etc.)
peuvent continuer à hériter explicitement de ces classes sans aucune
modification : hériter d'un Protocol est une syntaxe Python valide et
se comporte comme un héritage normal.
"""
from typing import Protocol, runtime_checkable

from models import Result


@runtime_checkable
class LogPort(Protocol):
    """Contrat pour services/log.py (LogService)."""

    def log(self, level: str, message: str) -> None: ...


@runtime_checkable
class MetricsPort(Protocol):
    """Contrat pour services/metrics.py (MetricsService)."""

    def incr_requests(self, endpoint: str = "/api/jarvis") -> None: ...
    def incr_pipeline_run(self) -> None: ...
    def incr_errors(self) -> None: ...
    def get_metrics(self) -> dict: ...


@runtime_checkable
class InferencePort(Protocol):
    """Contrat pour services/inference.py (InferenceService).

    Ne pas confondre avec services.adapters.protocols.InferenceAdapter :
    celui-ci est le contrat de la façade InferenceService, l'autre celui
    de chaque backend individuel (Ollama, Shimmy, ...).
    """

    def query(self, prompt: str, model: str, system: str | None = None) -> str: ...
    def query_multimodal(self, model: str, prompt: str, image_base64: str) -> dict: ...
    def chat(self, model: str, messages: list[dict]) -> Result: ...
    def is_available(self, model: str) -> bool: ...
    def first_available(self) -> str | None: ...
    def get_active_backend(self) -> str: ...
    def list_models(self) -> list[str]: ...
    def embed(self, text: str, model: str | None = None) -> list[float]: ...
    def ping(self) -> bool: ...


@runtime_checkable
class VectorPort(Protocol):
    """Contrat pour services/vector.py (VectorService)."""

    def index(self, text: str, metadata: dict = None) -> None: ...
    def index_batch(self, documents: list[tuple[str, dict | None]]) -> None: ...
    def vectorize_pending(self) -> int: ...
    def search(self, query: str, top_k: int = 5) -> list[dict]: ...
    def stats(self) -> dict: ...
    def preload(self) -> None: ...
    def is_healthy(self) -> bool: ...


@runtime_checkable
class MemoryPort(Protocol):
    """Contrat pour services/memory.py (MemoryService)."""

    def get_habits(self, limit: int = 10) -> list[dict]: ...
    def update_habits(self, entry: dict) -> None: ...
    def is_healthy(self) -> bool: ...


@runtime_checkable
class AnalyticsPort(Protocol):
    """Contrat pour services/analytics.py (AnalyticsService)."""

    def track_query(
        self, agent: str, model: str, tokens_in: int = 0,
        tokens_out: int = 0, latency_ms: float = 0, success: bool = True,
    ) -> None: ...
    def get_stats(self) -> dict: ...
    def get_most_used(self) -> dict: ...


@runtime_checkable
class ConversationPort(Protocol):
    """Contrat pour services/conversation.py (ConversationService)."""

    def create(self, title: str = "Nouvelle conversation") -> str: ...
    def add_message(
        self, conv_id: str, role: str, content: str,
        agent: str = None, model: str = None, backend: str = None,
    ) -> None: ...
    def get_conversation(self, conv_id: str) -> dict | None: ...
    def list_all(self) -> list[dict]: ...
    def delete(self, conv_id: str) -> None: ...
    def delete_all(self) -> None: ...
    def is_healthy(self) -> bool: ...
